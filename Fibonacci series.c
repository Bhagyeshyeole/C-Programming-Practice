//Program to print Fibonacci series of numbers.
#include <stdio.h>
#include <conio.h>
int main() {
    int n = 10; // Number of terms
    int t1 = 0, t2 = 1, nextTerm;

    printf("Fibonacci Series up to %d terms:\n", n);

    for (int i = 1; i <= n; ++i) {
        printf("%d ", t1);
        nextTerm = t1 + t2;
        t1 = t2;
        t2 = nextTerm;
    }

    return 0;
}
